package server

import (
	v1 "chat/api/group/service/v1"
	"chat/app/group/service/internal/conf"
	"chat/app/group/service/internal/service"
	"github.com/go-kratos/kratos/v2/log"
	"github.com/go-kratos/kratos/v2/middleware/recovery"
	"github.com/go-kratos/kratos/v2/transport/http"
	"github.com/go-kratos/swagger-api/openapiv2"
)

// NewHTTPServer new a HTTP server.
func NewHTTPServer(c *conf.Server, groupService *service.GroupService, logger log.Logger) *http.Server {
	var opts = []http.ServerOption{
		http.Middleware(
			recovery.Recovery(),
		),
	}
	if c.Http.Network != "" {
		opts = append(opts, http.Network(c.Http.Network))
	}
	if c.Http.Addr != "" {
		opts = append(opts, http.Address(c.Http.Addr))
	}
	if c.Http.Timeout != nil {
		opts = append(opts, http.Timeout(c.Http.Timeout.AsDuration()))
	}

	openAPIHandler := openapiv2.NewHandler()

	srv := http.NewServer(opts...)
	srv.HandlePrefix("/q/", openAPIHandler)
	v1.RegisterGroupHTTPServer(srv, groupService)
	return srv
}
